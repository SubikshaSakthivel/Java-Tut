public class foreach {
  public static void main(String args[])
  {
    int num[]={1,2,3,4,5};
    for(int i :num)
    {
      System.out.println("The flow of numbers are:"+i);
    }


  }
  
}
/*OUTPUT:
The flow of numbers are:1
The flow of numbers are:2
The flow of numbers are:3
The flow of numbers are:4
The flow of numbers are:5
 */
